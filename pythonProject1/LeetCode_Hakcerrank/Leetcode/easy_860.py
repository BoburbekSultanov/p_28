from typing import List


def lemonadeChange(bills: List[int]) -> bool:
    c5 = 1 if bills[0] == 5 else 0
    c10 = 1 if bills[0] == 10 else 0
    for i in range(1, len(bills)):
        if bills[i] == 5:
            c5 += 1
        elif bills[i] == 10:
            c10 += 1
            c5 -= 1
            if c5 < 0:
                return False
        elif bills[i] == 20:
            if c5 >= 1:
                if c10 >= 1:
                    c10 -= 1
                    c5 -= 1
                elif c5 >= 3:
                    c5 -= 3
                else:
                    return False

            else:
                return False
    return True if not (len(bills) == 1 and bills[0] != 5) else False


